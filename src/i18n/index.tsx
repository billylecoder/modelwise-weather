const translations: Record<Lang, Partial<Record<TranslationKey, string>>> = {
  en, el, es, fr, de, it, pt, nl, pl, tr, ru, ja, zh,
};

interface I18nContextType {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: TranslationKey) => string;
}

const I18nContext = createContext<I18nContextType>({
  lang: "en",
  setLang: () => {},
  t: (key) => key,
});

export const I18nProvider = ({ children }: { children: React.ReactNode }) => {
  const [lang, setLang] = useState<Lang>(() => {
    if (typeof window === "undefined") return "en";
    const saved = window.localStorage.getItem("lang") as Lang | null;
    return saved && translations[saved] ? saved : "en";
  });

  const updateLang = useCallback((l: Lang) => {
    setLang(l);
    try { window.localStorage.setItem("lang", l); } catch { /* ignore */ }
  }, []);

  const t = useCallback(
    (key: TranslationKey): string => {
      return translations[lang]?.[key] ?? en[key] ?? key;
    },
    [lang]
  );

  return (
    <I18nContext.Provider value={{ lang, setLang: updateLang, t }}>
      {children}
    </I18nContext.Provider>
  );
};

